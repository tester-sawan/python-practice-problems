"""
Q6. Accept two numbers from the user and display:
Sum
Difference
Product
Float division
Integer division
Remainder
"""
num1=int(input("Enter number 1: "))
num2=int(input("Enter number 2: "))
_sum = num1+num2
_difference = num1-num2
_product = num1*num2
_floatDivision = num1/num2
_integerDivision = num1//num2
_remainder = num1%num2
print("Sum:",_sum)
print("Difference:",_difference)
print("Product:",_product)
print("Float Division:",_floatDivision)
print("Integer Division:",_integerDivision)
print("Remainder:",_remainder)


